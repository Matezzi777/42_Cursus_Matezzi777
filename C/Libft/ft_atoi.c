int	ft_atoi(char *str);

int	ft_atoi(char *str)
{
	int	n;
	int	i;

	n = 0;
	i = 0;
	if (str[0] == '-')
		i++;
	while ((str[i] >= '0' && str[i] <= '9'))
	{
		n = n * 10;
		n = n + str[i] - '0';
		i++;
	}
	if (str[0] == '-')
		n = -n;
	return (n);
}
