int	ft_atoi(char	*str)
{
	int	nombre;
	int	i;

	nombre = 0;
	i = 0;
	while (str[i] >= '0' && str[i] <= '9')
	{
		nombre = nombre * 10;
		nombre = nombre + str[i] - '0';
		i++;
	}
	return (nombre);
}